public class TypeCasting {
    public static void main(String[] args) {
        System.out.println("Type Casting");

//        Automatic Conversion

        int i=50;
        double d=i;

        System.out.println(i);
        System.out.println(d);

//        Forceful Conversion
        double myval=34.25;
        int myint=(int)myval;

        System.out.println(myval);
        System.out.println(myint);

    }
}
